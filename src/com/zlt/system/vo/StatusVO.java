package com.zlt.system.vo;

import java.sql.Date;

/**
*审批状态查询的中间层类
*用于封装审批状态对象
*@author xinzou
*@date 20180124
*/

public class StatusVO {
	private String app_abb;//报销编号
	private String emp_name;//申请人
	private Date travel_time;//申请时间
	private String reim_type;//报销类型
	private float travel_money;//报销金额
	private String status_result;//审批结果

	public String getApp_abb() {
		return app_abb;
	}

	public void setApp_abb(String app_abb) {
		this.app_abb = app_abb;
	}

	public String getEmp_name() {
		return emp_name;
	}

	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}

	public Date getTravel_time() {
		return travel_time;
	}

	public void setTravel_time(Date travel_time) {
		this.travel_time = travel_time;
	}

	public String getReim_type() {
		return reim_type;
	}

	public void setReim_type(String reim_type) {
		this.reim_type = reim_type;
	}

	public float getTravel_money() {
		return travel_money;
	}

	public void setTravel_money(float travel_money) {
		this.travel_money = travel_money;
	}

	public String getStatus_result() {
		return status_result;
	}

	public void setStatus_result(String status_result) {
		this.status_result = status_result;
	}

	public StatusVO() {
		super();
		// TODO Auto-generated constructor stub
	}

	public StatusVO(String app_abb, String emp_name, Date travel_time, String reim_type, float travel_money,
			String status_result) {
		super();
		this.app_abb = app_abb;
		this.emp_name = emp_name;
		this.travel_time = travel_time;
		this.reim_type = reim_type;
		this.travel_money = travel_money;
		this.status_result = status_result;
	}

	@Override
	public String toString() {
		return "StatusVO [app_abb=" + app_abb + ", emp_name=" + emp_name + ", travel_time=" + travel_time
				+ ", reim_type=" + reim_type + ", travel_money=" + travel_money + ", status_result=" + status_result
				+ "]";
	}
	
}
